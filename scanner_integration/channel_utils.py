def resolve_name(name):
    """
    解析频道名：先去画质后缀，再用共享别名引擎匹配，最后做分类。
    返回 (标准名, 分类) 元组。
    """
    name = _alias_module.strip_quality_suffix(name)

    # 使用共享别名引擎匹配（O(1) 字典 + 预编译正则）
    std_name = _alias_module.match_channel_name(name)
    if std_name:
        cat = CATEGORY_FROM_TEMPLATE.get(std_name)
        if cat is None:
            cat = STANDARD_CATEGORY.get(std_name, classify_channel(std_name))
        return std_name, cat

    # CCTV 后备归一
    if 'CCTV' in name.upper() or '中央' in name or '野外' in name or '法治' in name or name.startswith('IPTV5+'):
        std = _alias_module.normalize_cctv_variant(name)
        if std.startswith('CCTV-'):
            cat = CATEGORY_FROM_TEMPLATE.get(std)
            if cat is None:
                cat = STANDARD_CATEGORY.get(std, classify_channel(std))
            return std, cat

    # 直接匹配模板或标准分类
    if name in CATEGORY_FROM_TEMPLATE:
        return name, CATEGORY_FROM_TEMPLATE[name]
    if name in STANDARD_CATEGORY:
        return name, STANDARD_CATEGORY[name]

    cat, prov = auto_classify(name)
    if cat == '卫视频道' and prov:
        return name, f"{prov}频道"
    return name, cat

