def channel_sort_key(ch, sort_name=None):
    cat = ch.get('category', '地方频道')
    name = sort_name if sort_name else ch.get('name', '')
    delay = ch.get('delay', 9999)
    delay = delay if isinstance(delay, (int, float)) else 9999

    cat_priority = {
        '央视频道': 0, '央视付费频道': 1, '卫视频道': 2,
        '影视频道': 3, '港澳台频道': 4,
    }
    base = cat_priority.get(cat, 5)

    if cat == '央视频道':
        num = extract_cctv_number(name)
        order = num if 1 <= num <= 17 else 999
        return (base, order, name.lower(), delay)
    elif cat in ('央视付费频道','卫视频道','影视频道','港澳台频道'):
        return (base, 0, name.lower(), delay)
    else:
        return (base, cat, name.lower(), delay)