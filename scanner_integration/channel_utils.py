def normalize_cctv_name(name):
    if not name:
        return name
    name = name.replace('——', '-').replace('—', '-').replace(' ', '').replace('_', '-')
    name = re.sub(r'CCTV\s*-\s*', 'CCTV-', name, flags=re.I)
    name = re.sub(r'CCTV\s*', 'CCTV-', name, flags=re.I)
    name = re.sub(r'-{2,}', '-', name)
    name = re.sub(r'CCTV[－—–]\s*(\d)', r'CCTV-\1', name, flags=re.I)
    name = re.sub(r'[a-z]*CCTV[-\s]*(\d+)', r'CCTV-\1', name, flags=re.I)
    return name

